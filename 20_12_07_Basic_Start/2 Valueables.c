#include <stdio.h>

int main(void)
{
	int x = 10;
	int y = 20;
	printf("x 는 %d 입니다.\n", x);
	printf("y 는 %d 입니다.\n", y);
	printf("x + y = %d 입니다.\n",x+y);
	printf("x - y = %d 입니다.\n",x-y);
	printf("x * y = %d 입니다.\n",x*y);
	printf("x / y = %d 입니다.\n",x/y);
	printf("x 나누기 y = %d 입니다.\n",x%y);
	return 0;
}
